// STATE
let currentLang = localStorage.getItem('homicidiosuy_lang') || 'es';

// INIT
document.addEventListener('DOMContentLoaded', () => {
    applyLanguage(currentLang);
});

// FUNCTIONS
window.toggleLanguageSelector = () => {
    const modal = document.getElementById('language-modal');
    if (!modal) return;
    if (modal.style.display === 'flex') {
        modal.style.display = 'none';
    } else {
        modal.style.display = 'flex';
    }
};

window.setLanguage = (lang) => {
    currentLang = lang;
    localStorage.setItem('homicidiosuy_lang', lang);
    applyLanguage(lang);
    window.toggleLanguageSelector(); // Close modal
    window.location.reload(); // Force reload to apply JS dynamic changes smoothly
};

function applyLanguage(lang) {
    const t = translations[lang];
    if (!t) return;

    // 1. Update Elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.getAttribute('data-translate');
        if (t[key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = t[key];
            } else {
                el.textContent = t[key];
            }
        }
    });

    // Update Page Title
    document.title = t.page_title;
}

// EXPORT TRANSLATION HELPER (Para usar en otros JS)
window.getTrans = (key) => {
    if (!translations[currentLang]) return key;
    return translations[currentLang][key] || key;
};
